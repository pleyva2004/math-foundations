def html_data() -> str:
    nodes = [
        {
            "id": c["id"],
            "slug": c["slug"],
            "title": c["title"],
            "level": c["level"],
            "summary": c["summary"],
            "color": LEVELS[c["level"]]["color"],
        }
        for c in CONCEPTS
    ]
    links = []
    for c in CONCEPTS:
        for prereq in c["prereqs"]:
            links.append({"source": prereq.split("-")[0], "target": c["id"]})
    return json.dumps({"nodes": nodes, "links": links}, indent=2)


def notebook() -> str:
    """Generate a minimal valid Jupyter notebook with one markdown+code cell pair per concept."""
    cells = [
        {
            "cell_type": "markdown",
            "metadata": {},
            "source": [
                "# Math Foundations — Interactive Notebook\n",
                "\n",
                "Progressive cells across all foundation concepts.\n",
                "Open in Colab or Jupyter Lab; run cells top-to-bottom or jump to a section.\n",
            ],
        }
    ]
    by_level = {}
    for c in CONCEPTS:
        by_level.setdefault(c["level"], []).append(c)
    for level_name in sorted(LEVELS.keys(), key=lambda n: LEVELS[n]["order"]):
        if level_name not in by_level:
            continue
        cells.append({
            "cell_type": "markdown",
            "metadata": {},
            "source": [f"## Level: {LEVELS[level_name]['label']}\n"],
        })
        for c in by_level[level_name]:
            cells.append({
                "cell_type": "markdown",
                "metadata": {},
                "source": [
                    f"### {c['id']}: {c['title']}\n",
                    "\n",
                    f"**Prereqs:** {', '.join(c['prereqs']) or '(none)'}\n",
                    "\n",
                    f"{c['summary']}\n",
                    "\n",
                    f"See `concepts/{c['id']}-{c['slug']}.md` for the full concept page.\n",
                ],
            })
            # Embed the code as a cell so the reader can execute it.
            code_path = ROOT / "code" / f"{c['id']}-{c['slug']}.py"
            if code_path.exists():
                src = code_path.read_text().splitlines(keepends=True)
            else:
                src = ["# (code not yet generated)\n"]
            cells.append({
                "cell_type": "code",
                "execution_count": None,
                "metadata": {},
                "outputs": [],
                "source": src,
            })
    nb = {
        "cells": cells,
        "metadata": {
            "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
            "language_info": {"name": "python", "version": "3.11"},
        },
        "nbformat": 4,
        "nbformat_minor": 5,
    }
    return json.dumps(nb, indent=1)


def write_if_managed_safe(path: Path, new_content: str):
    """Write `new_content` to `path`. If the file exists and contains the
    MARKER, preserve everything below the marker (hand-written content).
    """
    if path.exists() and MARKER in path.read_text():
        existing = path.read_text()
        below = existing.split(MARKER, 1)[1]
        path.write_text(new_content.split(MARKER, 1)[0] + MARKER + below)
    else:
        path.write_text(new_content)


def main():
    (ROOT / "concepts").mkdir(exist_ok=True)
    (ROOT / "code").mkdir(exist_ok=True)
    (ROOT / "html").mkdir(exist_ok=True)
    (ROOT / "notebook").mkdir(exist_ok=True)

    written = 0
    for c in CONCEPTS:
        md_path = ROOT / "concepts" / f"{c['id']}-{c['slug']}.md"
        py_path = ROOT / "code" / f"{c['id']}-{c['slug']}.py"
        write_if_managed_safe(md_path, concept_md(c))
        # .py files: only write if missing (preserve hand-edits entirely).
        if not py_path.exists():
            py_path.write_text(concept_py(c))
            written += 1
        else:
            # Don't clobber hand-written code.
            pass

    (ROOT / "README.md").write_text(readme())
    (ROOT / "html" / "data.json").write_text(html_data())
    (ROOT / "notebook" / "foundations.ipynb").write_text(notebook())

    print(f"Generated:")
    print(f"  concepts/: {len(CONCEPTS)} .md files (header refreshed; hand-content preserved)")
    print(f"  code/:     {written} new .py files (existing preserved)")
    print(f"  README.md, html/data.json, notebook/foundations.ipynb")


if __name__ == "__main__":
    main()
