"""Generate concept .md, code .py, README.md, html/data.json, and notebook
from manifest.json.

Idempotent: re-running overwrites generated files but never deletes hand-edited
ones. Concept files use a "do not edit above this line" marker so a subsequent
generator run can replace the auto-generated header without losing manual content.
"""

import json
import os
import re
from pathlib import Path

ROOT = Path(__file__).parent
MANIFEST = json.loads((ROOT / "manifest.json").read_text())
CONCEPTS = MANIFEST["concepts"]
LEVELS = MANIFEST["levels"]

MARKER = "<!-- ⬇️  Below this line: hand-written content. Above: auto-generated by generate.py — do not edit. -->"


def concept_md(node) -> str:
    """Generate the auto-managed header of a concept .md file."""
    prereq_links = ", ".join(f"[[{p}]](../concepts/{p}.md)" for p in node["prereqs"]) or "_(none — entry point)_"
    used_by = [c["id"] + "-" + c["slug"] for c in CONCEPTS if (node["id"] + "-" + node["slug"]) in c["prereqs"]]
    used_links = ", ".join(f"[[{u}]](../concepts/{u}.md)" for u in used_by) or "_(leaf node so far)_"
    return f"""# {node['title']}

**Level:** `{node['level']}`
**Concept ID:** `{node['id']}-{node['slug']}`
**Prerequisites:** {prereq_links}
**Used by:** {used_links}

## Plain-English intro
{node['summary']}

## Formal definition
$$
\\text{{See hand-written content below.}}
$$

## Why this matters
_(See below — connects to specific paper/improvement concepts.)_

## Code
The aligned code lives at [`../code/{node['id']}-{node['slug']}.py`](../code/{node['id']}-{node['slug']}.py).
It demonstrates the concept on a discrete or finite example, runnable in <30 s on CPU.

## Try it yourself
_(See below.)_

## Further reading
_(See below.)_

{MARKER}

<!-- Add hand-written content below: full plain-English exposition,
     formal LaTeX definition, why-it-matters paragraph tying to specific
     papers, code description with expected output, exercises, references. -->
"""


