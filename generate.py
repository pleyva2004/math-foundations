def mermaid_graph() -> str:
    """Generate the full mermaid DAG covering all concepts + edges."""
    lines = ["```mermaid", "graph TD"]
    # Style classes per level
    for level_name, info in LEVELS.items():
        lines.append(f"  classDef {level_name} fill:{info['color']},stroke:#333,stroke-width:1px;")
    # Nodes
    for c in CONCEPTS:
        node_id = c["id"]
        title = c["title"].replace('"', "'")
        lines.append(f'  {node_id}["{node_id}: {title}"]')
    # Edges
    for c in CONCEPTS:
        for prereq in c["prereqs"]:
            prereq_id = prereq.split("-")[0]
            lines.append(f"  {prereq_id} --> {c['id']}")
    # Level classes
    for c in CONCEPTS:
        lines.append(f"  class {c['id']} {c['level']};")
    # Click directives
    for c in CONCEPTS:
        lines.append(f'  click {c["id"]} "concepts/{c["id"]}-{c["slug"]}.md" "{c["title"]}"')
    lines.append("```")
    return "\n".join(lines)


